#include "../inc/heartbeat.h"
#include <avr/io.h>


////////////////////////////////////////////////////////////////////////////////
// Defines
////////////////////////////////////////////////////////////////////////////////

#if defined (__AVR_ATmega162__)
    #define HEARTBEAT_DDR  DDRE
    #define HEARTBEAT_PORT PORTE
    #define HEARTBEAT_PIN  PE0
#endif

void heartbeat_init(void) {
    HEARTBEAT_DDR |= 1 << HEARTBEAT_PIN;
}

void heartbeat(void) {
    HEARTBEAT_PORT ^= 1 << HEARTBEAT_PIN;
}
